#include "libft.h"

char	*ft_strrchr(const char *p, int c)
{
	int	i;

	i = 0;
	while (p[i] != '\0')
		i++;
	while (i >= 0)
	{
		if (p[i] == (char)c)
			return ((char *)(p + i));
		i--;
	}
	return (NULL);
}
